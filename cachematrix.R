## The function prints the inverse of a matrix. If an inverse has
## already been calculated, then the function gets the inverted result
## and skips the process. If else, the function computes the inverse
## and stores it in the cache

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  inv <- x$getinverse()
  if(!is.null(inv)) {
    message("getting cached data.")
    return(inv)
  }
  data <- x$get()
  inv <- solve(data)
  x$setinverse(inv)
  inv
}
